package calcolatrice;

public class Calcolatrice {
    public double CalcolaSomma(double a,double b) {
    	
    	return a+b;
    	}// calcola somma
    public double CalcolaSottrazione(double a, double b) {
    	return a-b;
    }// metodo sottrazione
    
    public double CalcolaProdotto(double a, double b) {
    	return a*b;
    }//metodo prodotto
    
    public double CalcolaDivisione(double a, double b) {
    	
    return a/b;
    }

}
